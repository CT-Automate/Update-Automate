def wait_for_monitor_ready(page, timeout=20000):
    """Wait for visible, stable React Flow nodes instead of a blind sleep."""
    try:
        if wait_for_visible_stable is None:
            page.locator(".react-flow__node").first.wait_for(state="visible", timeout=timeout)
            return
        wait_for_visible_stable(
            page,
            [".react-flow__node"],
            timeout=timeout,
            stable_ms=850,
            label="nexis_monitor",
        )
        print("Monitor ready (React Flow nodes visible and stable).")
    except Exception as exc:
        # Preserve current best-effort capture behavior; compute_clip() keeps a
        # safe fallback if the graph cannot be measured.
        print(f"WARNING: monitor DOM did not settle before capture: {exc}")


def capture(page, filepath):
    """Wait for dashboard DOM readiness and capture a dynamically clipped shot."""
    wait_for_monitor_ready(page)

    clip = compute_clip(page)
    page.screenshot(
        path=str(filepath),
        clip=clip
    )

    print(f"Saved: {filepath.name}  (clip {int(clip['width'])}x{int(clip['height'])})")


def step(name, fn, failures):
    """Run one capture step in isolation.

    A failed shot is logged and recorded instead of aborting the run, so the
    remaining shots are still attempted and metadata is still written.
    Timing is telemetry only and never changes the result of a capture step.
    Returns True on success, False on failure.
    """
    started = time.perf_counter()
    status = "ok"
    for attempt in range(1, 4):
        try:
            fn()
            print(f"[OK]   {name}")
            status = "ok"
            break
        except Exception as e:
            print(f"[FAIL] {name} (Attempt {attempt}): {e}")
            if attempt == 3:
                status = "failed"
                failures.append((name, str(e)))
                break
            time.sleep(1)

    log_timing(
        "nexis_capture",
        f"shot:{name}",
        time.perf_counter() - started,
        status=status,
    )
    return status == "ok"


